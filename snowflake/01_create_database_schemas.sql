  COMMENT = 'Governance metadata, classifications, audit results, alerts, and steward assignments.';

CREATE SCHEMA IF NOT EXISTS HC_GOV_DEMO.ANALYTICS
  COMMENT = 'Dashboard-facing views and KPI outputs.';

