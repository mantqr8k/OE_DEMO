-- Phase 1: Snowflake sandbox database and schema setup.
-- Run after 00_setup_roles_warehouse.sql.
-- Recommended execution role: SYSADMIN.

USE ROLE SYSADMIN;
USE WAREHOUSE HC_GOV_WH;

CREATE DATABASE IF NOT EXISTS HC_GOV_DEMO
  COMMENT = 'Healthcare Data Governance Demo sandbox database.';

CREATE SCHEMA IF NOT EXISTS HC_GOV_DEMO.BRONZE
  COMMENT = 'Raw ingestion layer for source-system data.';

